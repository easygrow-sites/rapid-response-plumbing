// Track used images to ensure uniqueness
const usedImages = new Set<string>();

export function getUniqueServiceImage(serviceSlug: string, index: number = 0): string {
  const images = serviceImages[serviceSlug] || serviceImages['default'];

  // Try to find an unused image
  for (const img of images) {
    if (!usedImages.has(img)) {
      usedImages.add(img);
      return img;
    }
  }

  // If all images used, return based on index
  const selectedImage = images[index % images.length];
  return selectedImage;
}

export function getServiceImage(serviceSlug: string, index: number = 0): string {
  const images = serviceImages[serviceSlug] || serviceImages['default'];
  return images[index % images.length];
}

export function resetUsedImages() {
  usedImages.clear();
}
